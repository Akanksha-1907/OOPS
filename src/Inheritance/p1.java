package Inheritance;

public class p1 
{
	public void trapezium(int a,int b,int h)
	{
		System.out.println("Area of trapezium- "+((a+b)/2)*h);
	}
	
	public static void cylinder(int r,int h)
	{
		System.out.println("Area of cylinder- "+(3.14*2*r*h));
	}
	
	public void sphere(int r)
	{
		System.out.println("Area of sphere- "+(3.14*4*r*4));
	}
	
}
