package Inheritance_with_constructor;

public class parent_2_super 
{
	public parent_2_super()
	{
		System.out.println("Hey!");
	}
	
	public parent_2_super(int a)
	{
		System.out.println("Number of dogs- "+a);
	}
	
	public parent_2_super(int a,int b)
	{
		System.out.println("Number of cats- "+a);
		System.out.println("Number of parrots- "+b);
	}
	
	public parent_2_super(int c,float j)
	{
		System.out.println("Number of hens- "+c);
		System.out.println("Number of ducks- "+j);
	}
	

}
